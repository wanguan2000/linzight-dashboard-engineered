import { spawnSync } from 'node:child_process';
import { mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputDir = resolve(repoRoot, process.argv[2] || 'exports/postgres-migration');
const databaseUrl = process.env.LINZIGHT_DATABASE_URL || `sqlite:///${resolve(repoRoot, 'backend/linzight_demo.db')}`;

mkdirSync(outputDir, { recursive: true });

const python = process.env.PYTHON || 'python3';
const script = `
import csv, json, os, sqlite3, sys
from pathlib import Path

database_url = os.environ["LINZIGHT_DATABASE_URL"]
if not database_url.startswith("sqlite:///"):
    raise SystemExit("sqlite-to-postgres-export expects LINZIGHT_DATABASE_URL=sqlite:///...")
db_path = database_url.replace("sqlite:///", "", 1)
out_dir = Path(os.environ["LINZIGHT_MIGRATION_EXPORT_DIR"])
out_dir.mkdir(parents=True, exist_ok=True)
conn = sqlite3.connect(db_path)
conn.row_factory = sqlite3.Row
tables = [row["name"] for row in conn.execute("SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name")]
manifest = []
copy_lines = [
    "-- Generated by npm run export:postgres-migration",
    "-- Review schema, constraints, indexes, and JSONB casts before production import.",
    "\\\\set ON_ERROR_STOP on",
    "BEGIN;",
]
schema_lines = [
    "-- PostgreSQL migration sketch generated from SQLite metadata.",
    "-- This is an operator-reviewed starting point, not a blind production migration.",
]
def pg_type(sqlite_type):
    value = (sqlite_type or "TEXT").upper()
    if "INT" in value:
        return "INTEGER"
    if "BLOB" in value:
        return "BYTEA"
    if "REAL" in value or "FLOA" in value or "DOUB" in value:
        return "DOUBLE PRECISION"
    return "TEXT"
def normalize_cell(value):
    if isinstance(value, bytes):
        return "\\\\x" + value.hex()
    return value
def normalize_row(row):
    return {key: normalize_cell(value) for key, value in dict(row).items()}
for table in tables:
    columns = [dict(row) for row in conn.execute(f"PRAGMA table_info({table})")]
    rows = [normalize_row(row) for row in conn.execute(f"SELECT * FROM {table}")]
    csv_path = out_dir / f"{table}.csv"
    json_path = out_dir / f"{table}.json"
    if rows:
        with csv_path.open("w", newline="", encoding="utf-8") as handle:
            writer = csv.DictWriter(handle, fieldnames=list(rows[0].keys()))
            writer.writeheader()
            writer.writerows(rows)
    else:
        csv_path.write_text("", encoding="utf-8")
    json_path.write_text(json.dumps(rows, ensure_ascii=False, indent=2), encoding="utf-8")
    column_names = [column["name"] for column in columns]
    schema_lines.append("")
    schema_lines.append(f"CREATE TABLE IF NOT EXISTS {table} (")
    field_lines = []
    for column in columns:
        field = "  " + column["name"] + " " + pg_type(column["type"])
        if column["pk"]:
            field += " PRIMARY KEY"
        elif column["notnull"]:
            field += " NOT NULL"
        field_lines.append(field)
    schema_lines.append(",\\n".join(field_lines))
    schema_lines.append(");")
    if column_names:
        copy_lines.append(f"\\\\copy {table} ({', '.join(column_names)}) FROM '{csv_path.name}' WITH (FORMAT csv, HEADER true);")
    manifest.append({"table": table, "rows": len(rows), "csv": csv_path.name, "json": json_path.name, "columns": column_names})
copy_lines.append("COMMIT;")
(out_dir / "manifest.json").write_text(json.dumps(manifest, ensure_ascii=False, indent=2), encoding="utf-8")
(out_dir / "load-postgres.sql").write_text("\\n".join(copy_lines) + "\\n", encoding="utf-8")
(out_dir / "schema-postgres.sql").write_text("\\n".join(schema_lines) + "\\n", encoding="utf-8")
(out_dir / "README.md").write_text(
    "# PostgreSQL migration export\\n\\n"
    "This directory contains CSV/JSON table exports plus schema-postgres.sql and load-postgres.sql. "
    "Use it as an operator-reviewed migration package: create the production schema, review JSON/JSONB and BYTEA columns, "
    "load into a staging database, run API smoke tests, then cut over with backups and rollback ready.\\n",
    encoding="utf-8",
)
print(f"Exported {len(tables)} tables to {out_dir}")
`;

const result = spawnSync(python, ['-c', script], {
  cwd: repoRoot,
  env: {
    ...process.env,
    LINZIGHT_DATABASE_URL: databaseUrl,
    LINZIGHT_MIGRATION_EXPORT_DIR: outputDir,
  },
  encoding: 'utf8',
  stdio: ['ignore', 'pipe', 'pipe'],
});

if (result.status !== 0) {
  process.stderr.write(result.stderr || result.stdout);
  process.exit(result.status ?? 1);
}

process.stdout.write(result.stdout);
